public class ArrayDemo1 {
    public static void main(String[] args) {
        int [] Array1 = {7,4,8};
        int [] Array2 = {9,0,2};

        System.out.println(Array1[0]+Array2[0]);
        System.out.println(Array1[1]+Array2[1]);
        System.out.println(Array1[2]+Array2[2]);
        System.out.println(Array1[0]*Array2[0]);
        System.out.println(Array1[1]*Array2[1]);
        System.out.println(Array1[2]*Array2[2]);
    }

}

