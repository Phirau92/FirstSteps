public class ArithmetischeOperationen

 {
     public static void main(String[] args) {
         System.out.println();
         int a = 10;
         int b = 6;
         System.out.println("a="+ a +",b=" + b);
         System.out.println(a + "+" + b + "="+ (a+b));
         System.out.println(a + "-" + b + "="+ (a-b));
         System.out.println(a + "*" + b + "="+ (a*b));
         System.out.println(a + "/" + b + "="+ (a/b));
         System.out.println(a + "%" + b + "="+ (a%b));



     }
}
